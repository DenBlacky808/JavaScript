/*
5. Реализовать основные 4 арифметические операции
в виде функций с двумя параметрами.
Обязательно использовать оператор return.
*/

function sumFunc(a, b) {
    return a + b
}

function diffFunc(a, b) {
    return a - b
}

function multFunc(a, b) {
    return a * b
}

function divFunc(a, b) {
    return a / b
}

console.log (
    sumFunc(5, 2),
    diffFunc(5, 2),
    multFunc(5, 2),
    divFunc(5, 2)
)
